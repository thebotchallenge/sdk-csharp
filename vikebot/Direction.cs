namespace vikebot
{
    /// <summary>
    /// Specifies a direction in an two-dimensional grid (the map)
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// In front of the player
        /// </summary>
        Forward = 0,

        /// <summary>
        /// Right side of the player
        /// </summary>
        Right = 1,

        /// <summary>
        /// At bottom of the player
        /// </summary>
        Backward = 2,

        /// <summary>
        /// Left side of the player
        /// </summary>
        Left = 3
    }
}
